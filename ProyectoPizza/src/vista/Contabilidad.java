    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:

        String cod = jTextFieldCod.getText();

        if (cod.matches("[0-9]{1,6}")) {

            ArrayList<Empleado> e = null;

            try {
                e = p.consultarEmp(jTextFieldCod.getText());
            } catch (SQLException ex) {
                Logger.getLogger(Contabilidad.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (e.isEmpty()) {

                JOptionPane.showMessageDialog(this, "El empleado con cÃ³digo: " + cod + " no existe",
                        "ERROR", JOptionPane.WARNING_MESSAGE);

                jTextFieldCod.setText(null);
                jTextFieldCod.grabFocus();

            } else {

                cargarDatos(cod);
                jPanel1.setVisible(true);
                jTextFieldCod.setEnabled(false);
                jButtonAceptar.setEnabled(false);

            }

        } else {

            JOptionPane.showMessageDialog(this, "El cÃ³digo debe ser un nÃºmero de mÃ¡ximo 6 digitos",
                    "ERROR", JOptionPane.ERROR_MESSAGE);

            jTextFieldCod.setText(null);
            jTextFieldCod.grabFocus();

        }

    }//GEN-LAST:event_jButtonAceptarActionPerformed

