    private void conectar() throws IOException, SQLException {
        Properties prop = new Properties();
        prop.load(new InputStreamReader(PersistenciaJDBC.class.getResourceAsStream("CFG.INI")));
        String s = prop.getProperty("tipoPersistencia").toLowerCase();

        switch (s) {

            case "mysqljdbc":
                p = new PersistenciaJDBC();
                break;

            case "hibernate":
                p = new PersistenciaHibernate();
                break;

            default:
                JOptionPane.showMessageDialog(this, "El tipo de persistencia no es vÃ¡lido (mysqlJDBC o Hibernate)",
                        "FATAL ERROR", JOptionPane.ERROR_MESSAGE);
                System.exit(0);

        }
    }

    private void cargarDatos(String cod) {

        DecimalFormat df = new DecimalFormat("#.00");

        try {

            ArrayList<String> data = p.cargarContabilidad(cod);

            jTextFieldNombre.setText(data.get(0));
            jTextFieldApe1.setText(data.get(1));
            jTextFieldApe2.setText(data.get(2));

            double horas = Double.parseDouble(data.get(3)) / 60;

            jTextFieldHoras.setText(df.format(horas));

            jTextFieldSal.setText(data.get(4));

            double total = horas * Double.parseDouble(data.get(4));

            jTextFieldTotal.setText(df.format(total));

        } catch (SQLException ex) {
            Logger.getLogger(Contabilidad.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
