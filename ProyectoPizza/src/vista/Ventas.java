    private void jButtonAcepActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButtonAcepActionPerformed
        // TODO add your handling code here:

        Vector resul = new Vector();
        Vector titColumn = new Vector();
        titColumn.addElement("Código");
        titColumn.addElement("Importe");

        try {

            switch (getSelectedButton()) {

                case 0:

                    resul = p.estadisticaPorFecha(dateChooserCombo.getText(), dateChooserCombo2.getText());

                    break;
                case 1:

                    if (!jTextFieldCod.getText().matches("[0-9]{1,6}")) {

                        JOptionPane.showMessageDialog(this, "El código debe ser un número de máximo 6 digitos",
                                "ERROR", JOptionPane.ERROR_MESSAGE);

                    } else {

                        resul = p.estadisticaPorCod("Cliente", jTextFieldCod.getText());

                    }
                    break;
                case 2:

                    if (!jTextFieldCod.getText().matches("[0-9]{1,6}")) {

                        JOptionPane.showMessageDialog(this, "El código debe ser un número de máximo 6 digitos",
                                "ERROR", JOptionPane.ERROR_MESSAGE);

                    } else {

                        resul = p.estadisticaPorCod("Empleado", jTextFieldCod.getText());

                    }
                    break;

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, e.getMessage());

        }

        if (resul.isEmpty()) {

            JOptionPane.showMessageDialog(this, "No se ha encontrado coincidencias", "Resultados",
                    JOptionPane.INFORMATION_MESSAGE);
            jTextFieldCod.setText(null);

        } else {

            DefaultTableModel dtm = new DefaultTableModel(resul, titColumn) {

                @Override
                public boolean isCellEditable(int row, int column) {

                    return false;
                }

            };

            jTable1.setModel(dtm);
            jTextFieldCod.setEnabled(false);
            dateChooserCombo.setEnabled(false);
            dateChooserCombo2.setEnabled(false);
            jButtonAcep.setEnabled(false);
            jButtonOtra.setEnabled(true);
            jRadioButtonCliente.setEnabled(false);
            jRadioButtonEmp.setEnabled(false);
            jRadioButtonFecha.setEnabled(false);

            actualizarTotal();

        }

    }// GEN-LAST:event_jButtonAcepActionPerformed

    private void actualizarTotal() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

        double total = 0;

        for (int i = 0; i < dtm.getRowCount(); i++) {

            double nuevo = Double.parseDouble(String.valueOf(dtm.getValueAt(i, 1)));

            total = total + nuevo;

        }

        jTextFieldTotal.setText(String.valueOf(total));

    }

