    private int getSelectedButton() {

        Enumeration<AbstractButton> consulta;

        int i = 0, selec = 0;

        for (consulta = buttonGroup1.getElements(); consulta.hasMoreElements();) {

            if (consulta.nextElement().isSelected()) {

                selec = i;

            }
            i++;
        }

        return selec;
    }
