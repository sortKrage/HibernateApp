    private boolean camposValidos() {

        boolean val = true;

        if (!jTextFieldNombre.getText().equals("")) {
            if (!jTextFieldApe.getText().equals("")) {
                if (!jTextFieldApe2.getText().equals("")) {
                    if (!jTextFieldDir.getText().equals("")) {

                    } else {

                        jTextFieldDir.grabFocus();
                        jLabelError.setVisible(true);
                        val = false;

                    }
                } else {

                    jTextFieldApe2.grabFocus();
                    jLabelError.setVisible(true);
                    val = false;
                }
            } else {

                jTextFieldApe.grabFocus();
                jLabelError.setVisible(true);
                val = false;
            }
        } else {

            jTextFieldNombre.grabFocus();
            jLabelError.setVisible(true);
            val = false;
        }

        return val;
    }
