    private void jButtonAcepActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButtonAcepActionPerformed
        // TODO add your handling code here:

        if (jTextFieldCod.getText().matches("[0-9]{1,6}")) {

            try {

                ArrayList<Cliente> cli = p.consultarCli(Integer.parseInt(jTextFieldCod.getText()));

                if (!cli.isEmpty()) {

                    jTextFieldNombre.setText(cli.get(0).getNombre());
                    jTextFieldApe.setText(cli.get(0).getApe1());
                    jTextFieldApe2.setText(cli.get(0).getApe2());
                    jTextFieldDir.setText(cli.get(0).getDireccion());
                    jPanel1.setVisible(true);
                    jLabelCabe.setText("Modificar Cliente: ");
                    jLabelCabe.setVisible(true);

                } else {

                    int i = Integer.parseInt(jTextFieldCod.getText());
                    jPanel1.setVisible(true);
                    jLabelCabe.setText("A침adir Cliente: ");
                    jLabelCabe.setVisible(true);

                }

                jTextFieldCod.setEnabled(false);
                jButtonAcep.setEnabled(false);

            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(this, ex.getMessage());

            }

        } else {

            JOptionPane.showMessageDialog(this, "El c칩digo debe ser un d칤gito de m치ximo 6 d칤gitos",
                    "ERROR", JOptionPane.ERROR_MESSAGE);

            jTextFieldCod.setText(null);
            jTextFieldCod.grabFocus();

        }

    }// GEN-LAST:event_jButtonAcepActionPerformed

